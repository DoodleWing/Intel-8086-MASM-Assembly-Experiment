DATA SEGMENT
BLOCK DW 12,-5,7,90,123,-12,36,314,129,10,565,234,2,3,-24,124,112,-125,23,0
POSNUM DW 21 DUP(0)
NEGNUM DW 21 DUP(0)
DATA ENDS

STACK SEGMENT STACK

STACK ENDS

CODE SEGMENT
	ASSUME DS:DATA,SS:STACK,CS:CODE
START:
	MOV AX,DATA
	MOV DS,AX
	LEA SI,BLOCK
	CLD
	MOV CX,20
	MOV BX,0
	MOV BP,0
for:
	LODSW
	CMP AX,0
	JS negn
	JMP posn
posn:
	ADD BX,2
	MOV [POSNUM+BX],AX
	JMP continue
negn:
	ADD BP,2
	MOV [NEGNUM+BP],AX
	JMP continue
continue:
	LOOP for	
	
exit:
	SAR BX,1
	SAR BP,1
	MOV POSNUM,BX
	MOV NEGNUM,BP
	MOV AH,4CH
	INT 21H
CODE ENDS
	END START