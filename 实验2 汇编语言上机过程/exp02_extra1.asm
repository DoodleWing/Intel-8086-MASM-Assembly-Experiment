DATA SEGMENT
STR DB 'Hello',68,-20,3 DUP(4),'$';按Byte分配变量BR
DATA ENDS

STACK SEGMENT STACK

STACK ENDS

CODE SEGMENT
ASSUME DS:DATA,SS:STACK,CS:CODE 
START:

MOV AX,DATA			;将DATA的值装入AX
MOV DS,AX			;将AX装入DS寄存器

LEA DX,STR			;将变量STR的偏移地址装入DX
MOV AH,9				;设置9号中断
INT 21H				;启动中断

CODE ENDS       　　
END START