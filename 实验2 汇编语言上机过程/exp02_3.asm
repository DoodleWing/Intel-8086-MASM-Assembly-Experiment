DATA SEGMENT

TABLE DW 10,20,30,40,50		;按Word分配变量TABLE
BUF DW 4						;按Word分配变量BUF

DATA ENDS

STACK SEGMENT STACK

STACK ENDS

CODE SEGMENT
ASSUME DS:DATA,SS:STACK,CS:CODE 
START:

MOV AX,DATA
MOV DS,AX

LEA BX,TABLE			;将TABLE的偏移地址装入BX
ADD BX,BUF			;相当于BX=TABLE的偏移地址+4
MOV AX,[BX]			;将BX偏移地址对应内存的内容装入AX
					;AX应为0030D，也就是001EH
LEA DX,[BX]			;将BX中保存的偏移地址装入DX
MOV AH,9				;设置9号中断
INT 21H				;启动中断

MOV AX,4C00H
INT 21H

CODE ENDS       　　
END START